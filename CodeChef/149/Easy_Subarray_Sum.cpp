void solve()
{
    int n; cin >> n;
    vii a(n);
    for(auto &x: a) cin >> x;
    int l = n, r = -1;
    for(int i = 0; i < n; i++)
        if(a[i] > 0)
        {
            l = i; break;
        } 
    for(int i = n-1; i >= 0; i--)
        if(a[i] > 0)
        {
            r = i; break;
        }

    int cnt = 0;
    for(int i = l; i <= r; i++) if(a[i] < 0) cnt++;
    cout << cnt << "\n";
}

