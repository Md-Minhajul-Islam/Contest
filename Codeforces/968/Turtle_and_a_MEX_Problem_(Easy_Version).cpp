void solve()
{
    ll n, m; cin >> n >> m;
    ll mx = -1;
    for(int i = 0; i < n; i++)
    {
    	int k; cin >> k;
    	vii v(k+5, 0);
    	for(int j = 0; j < k; j++) 
    	{
    		int x; cin >> x;
    		if(x < k+5) v[x] = 1;
    	}
    	ll j = 0;
    	while(v[j]) j++;
    	j++;
    	while(v[j]) j++;
    	mx = max(mx, j);
    }
    debug(mx);
    ll ans;
    if(m <= mx) ans = 1ll*(m+1)*mx;
    else ans = 1ll*mx*mx+1ll*(mx+m)*(m-mx+1)/2; 
    cout << ans << "\n";
}

