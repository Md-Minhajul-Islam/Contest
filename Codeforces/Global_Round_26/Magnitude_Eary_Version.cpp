void solve()
{
	ll n; cin >> n;
	ll sum = 0, mn = 0;
	for(int i = 0; i < n; i++)
	{
		ll a; cin >> a;
		sum += a;
		mn = min(sum, mn);
	}
	cout << sum-2ll*mn <<"\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t; cin >> t;
    while(t--)
    {
        solve();
    }
    return 0;
}