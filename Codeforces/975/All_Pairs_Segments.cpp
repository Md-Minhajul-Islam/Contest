void solve()
{
    ll n, q; cin >> n >> q;
    vll a(n);
    for(auto &x: a) cin >> x;
    map<ll, ll> m;
    for(int i = 0; i < n; i++)
    {
        ll x = 1ll*i*(n-i)+(n-1-i);
        ll y = 1ll*(n-1-i)*(i+1);
        m[x]++;
        if(i < n-1) m[y] += 1ll*a[i+1]-a[i]-1;
    }
    while(q--)
    {
        ll k; cin >> k;
        cout << m[k] <<" ";
    }
    cout << "\n";

}

