void solve()
{
    int n; cin >> n;
    ll pref[n+1]; pref[0] = 0ll;
    for(int i = 1; i <= n; i++)
    {
    	cin >> pref[i];
    	pref[i] += pref[i-1];
    }
    ll sum = 0;
    string s; cin >> s;
    ll l = 0, r = n-1;
    while(l < r)
    {
    	while(l < n && s[l] != 'L') l++;
    	while(r >= 0 && s[r] != 'R') r--;
    	if(l < r) sum += 0ll+pref[r+1]-pref[l];
    	l++, r--;
    }
    cout << sum << "\n";
}

